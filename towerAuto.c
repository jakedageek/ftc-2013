//TOWER AUTONOMOUS
int sonarvalue = 0;
void autoStraight();
void autoDiag();
void autoDiagR();
void autoHoriz();

//if the end goal is straight ahead
void autoStraight(){
	int a = 0;
	while (true){
		writeDebugStreamLine("%d",SensorValue[Sonar]);
		motor[leftDrive] = -20;
		motor[rightDrive] = -20;
		if(SensorValue[Sonar] < 30){
			a ++;
		}
		if(a > 7){
			motor[leftDrive] = 0;
			motor[rightDrive]= 0;
			break;
		}
	}
	return;
}

void autoDiag(){
	driveBackwardDist(24, 20);	//drive out from the
	wait1Msec(100);
	turnEuler(90,70,true); //turn right
	wait1Msec(100);
	driveBackwardDist(28, 20); //drive left backwards
	wait1Msec(100);
	turnEuler(135,70,false); //turn towards the goal
	wait1Msec(100);
	driveBackwardDist(18, 20); // move towards the goal
	wait1Msec(5000);
	//pole
	driveForwardDist(15, 20); //drive back
	wait1Msec(100);
	turnEuler(90,70,false); //turn towards pole
	wait1Msec(100);
	driveBackwardDist(10, 20); //drive towards pole
	wait1Msec(100);
	turnEuler(90,70,true); //turn towards pole
	wait1Msec(100);
	driveBackwardDist(20,40); //drive against pole and knock it down
	return;
}

void autoDiagR(){
	turnEuler(45, 70, true);
	driveForward(3000, 20);
	return;
}

void autoHoriz(){
	driveBackwardDist(24,20); //drive out of zone
	wait1Msec(100);
	turnEuler(45,70,true); //turn diagonally towards goal
	wait1Msec(100);
	driveBackwardDist(30,20); //drive backwards
	wait1Msec(100);
	turnEuler(135,70,false); //turn directly towards goal
	wait1Msec(100);
	driveBackwardDist(15,20); //drive towards to goal
	wait1Msec(5000);
	//pole
	driveForwardDist(15, 20); //drive back
	wait1Msec(100);
	turnEuler(90,70,false); //turn towards pole
	wait1Msec(100);
	driveBackwardDist(10, 20); //drive towards pole
	wait1Msec(100);
	turnEuler(90,70,true); //turn towards pole
	wait1Msec(100);
	driveBackwardDist(20,40); //drive against pole and knock it down
	return;
}


void initializeRobot(){
	calibrateGyro();
	nMotorEncoder[liftLeft] = 0;
	//RESET SERVOS

	return;
}

task main()
{
	int i = 0;
	int j = 0;
	int k = 0;
	initializeRobot();
	//waitForStart();
	while(true){
		sonarvalue = SensorValue[Sonar];
		writeDebugStreamLine("%d", sonarvalue);
		if(sonarvalue == 255){
			//Diagonal center console
			i++;
			if(i > 5){
				nxtDisplayCenteredTextLine(3, "Diagonal, %d", sonarvalue);
				autoDiag();
				wait1Msec(2000);
				autoDiagR();
				break;
			}
			//The ultrasonic sensor cannot detect diagonal surfaces - therefore, it returns 255 as its default value.

		}else if(SensorValue[Sonar] < 115){
			//goal is straight ahead
			j++;
			writeDebugStreamLine("%d", j);
			if(j > 5){
				nxtDisplayCenteredTextLine(3, "Ahead, %d", sonarvalue);
				autoStraight();
				break;
			}
		}else{
			//goal is sideways
			k++;
			if(k > 5){
				nxtDisplayCenteredTextLine(3, "Sideways, %d", sonarvalue);
				break;
			}
		}
	}
	PlaySoundFile("attention.rso");
}
